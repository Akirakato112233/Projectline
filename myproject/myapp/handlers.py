def extract_subject_and_category(user_text: str):
    normalized_text = " ".join(user_text.strip().split())
    subject = next(
        (canonical for alias, canonical in ZODIAC_ALIASES.items() if alias in normalized_text),
        None,
    )
    category = next(
        (canonical for alias, canonical in CATEGORY_ALIASES.items() if alias in normalized_text),
        None,
    )
    return subject, category


def build_zodiac_reply(user_text: str) -> str:
    subject, category = extract_subject_and_category(user_text)

    if not subject or not category:
        return "พิมพ์แบบนี้ได้เลย: ราศีเมษ โชคลาภ"

    record = (
        ZodiacKnowledge.objects.filter(subject=subject, category=category)
        .order_by("-published_at", "-id")
        .first()
    )
    if record is None:
        return f"ยังไม่มีข้อมูล{subject} เรื่อง{category}"

    return f"{subject} {category}\n{record.content}"


def handle_text_message(user_text: str) -> TextMessage:
    return TextMessage(text=build_zodiac_reply(user_text))
